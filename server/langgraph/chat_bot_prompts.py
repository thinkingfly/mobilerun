"""聊天 Bot Agent 提示词模板。"""

# ── 系统提示词 ──
# 用于生成回复时的角色设定
CHAT_BOT_SYSTEM_PROMPT = """你是{device_user}，一个友好的智能聊天助手。

你的性格特点：
- 友善、幽默、有耐心
- 回复简洁明了，通常不超过 3 句话
- 根据对话氛围调整语气（正式/轻松/幽默）
- 用中文回复，除非对方用其他语言

回复规则：
1. 仔细阅读聊天记录，理解上下文
2. 针对最后一条消息进行回复
3. 如果是群聊，注意区分不同发言者
4. 避免重复回复已经回复过的内容
5. 如果不确定如何回复，可以询问或保持沉默
6. 不要暴露你是 AI 的身份，除非被直接问到
7. 如果最后一条消息不需要回复（如表情、简短回应），可以选择不回复

输出格式：只输出回复内容，不要包含任何解释或前缀。"""

# ── 读取聊天消息提示词 ──
# 用于从设备屏幕读取聊天记录
READ_CHAT_PROMPT = """请读取当前聊天窗口中所有可见的消息。

当前用户（自己发送的消息）的名字是：{device_user}

对于每条消息，提取以下信息：
- sender: 发送者昵称
- content: 消息内容
- time: 消息时间（如果可见，否则留空）
- is_self: 是否是自己发送的消息（如果 sender 是 {device_user} 则为 true）

返回 JSON 数组格式：
[{{"sender": "昵称", "content": "消息内容", "time": "时间", "is_self": false}}, ...]

注意：
1. 按照消息从上到下的顺序排列
2. 通过对比 sender 和 {device_user} 来区分自己和对方的消息
3. 如果消息是图片，描述为 [图片]
4. 如果消息是表情/贴纸，描述为 [表情]
5. 如果消息是语音，描述为 [语音]
6. 如果消息是视频，描述为 [视频]
7. 如果消息是链接，保留完整链接文本"""

# ── 发送回复提示词 ──
# 用于在设备上发送消息
SEND_REPLY_PROMPT = """请在当前聊天窗口发送以下消息：

{reply}

操作步骤：
1. 点击底部的输入框
2. 输入上述消息内容
3. 点击发送按钮
4. 确认消息已成功发送

注意：
- 如果输入框没有显示，可能需要先点击屏幕底部的输入区域
- 确保消息完整输入后再点击发送
- 发送后确认消息出现在聊天记录中"""

# ── 打开聊天软件提示词 ──
OPEN_APP_PROMPT = """请打开{app_name}应用。

如果应用已经在运行，确保它在前台显示。
如果应用未安装，报告错误。"""

# ── 进入聊天窗口提示词 ──
# 用于导航到指定的聊天对象
ENTER_CHAT_PROMPT = """请在{app_name}中找到并进入与"{target_chat}"的聊天窗口。

查找策略（按优先级尝试）：
1. 首先在聊天列表中上下滑动浏览，看是否能直接找到"{target_chat}"
2. 如果列表中没有找到，使用搜索功能：点击搜索图标，输入"{target_chat}"进行搜索
3. 点击搜索结果或列表中的对应聊天
4. 确认已进入正确的聊天窗口

注意：
- 优先尝试滑动列表查找，因为目标可能就在列表中只是不在当前可见区域
- 如果是群聊，确保群名匹配
- 如果是私聊，确保联系人名匹配
- 滑动时每次滑动幅度不要太大，以便能看清列表内容
- 如果多次搜索仍找不到，再报告错误"""

# ── 判断聊天类型提示词 ──
# 用于判断当前是单聊还是群聊
DETECT_CHAT_TYPE_PROMPT = """请判断当前聊天窗口是单聊还是群聊。

观察以下特征：
- 群聊通常有多个不同的发送者
- 群聊顶部可能显示群名和成员数
- 单聊只有一个对话对象

返回 JSON 格式：
{{"chat_type": "single" 或 "group", "chat_name": "群名或联系人名"}}"""
