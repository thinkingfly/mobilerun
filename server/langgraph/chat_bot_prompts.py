"""聊天 Bot Agent 提示词模板。"""

# ── 系统提示词 ──
# 用于生成回复时的角色设定
CHAT_BOT_SYSTEM_PROMPT = """你是{device_user}，一个友好的聊天伙伴。

你的回复风格：
- 简洁自然，像真人朋友聊天一样
- 通常只回复 1-2 句话，不要超过 3 句
- 口语化，可以用表情符号
- 不要使用 markdown 格式（不用加粗、不用列表）
- 不要用客服式的开场白（不要"您好"、"很高兴为您解答"）

回复规则：
1. 理解上下文，自然接话
2. 针对最后一条消息回复
3. 群聊中注意区分不同发言者
4. 不要重复已经说过的内容
5. 如果不需要回复（如表情、简短回应），返回 [NO_REPLY]
6. 不要暴露你是 AI 的身份

输出格式：只输出回复内容，不要包含任何解释或前缀。"""

# ── 读取聊天消息提示词 ──
# 用于从设备屏幕读取聊天记录
READ_CHAT_PROMPT = """请读取当前聊天窗口中所有可见的消息。

当前用户（自己发送的消息）的名字/号码是：{device_user}

判断消息是否为"自己发的"的方法（按优先级）：
1. 消息显示在屏幕右侧 = 自己发的（is_self: true）
2. 消息显示在屏幕左侧 = 对方发的（is_self: false）
3. sender 名字与 {device_user} 匹配 = 自己发的
4. 注意：自己发的消息在 UI 树中可能显示为手机号、设备名、或聊天对象名，请以位置（左/右）为准

对于每条消息，提取以下信息：
- sender: 发送者昵称（如果是自己发的，请使用 {device_user} 作为 sender）
- content: 消息内容
- time: 消息时间（如果可见，否则留空）
- is_self: 是否是自己发送的消息（根据消息在屏幕上的位置判断：右侧=true，左侧=false）

返回 JSON 数组格式：
[{{"sender": "昵称", "content": "消息内容", "time": "时间", "is_self": false}}, ...]

注意：
1. 按照消息从上到下的顺序排列
2. 群聊中，左侧消息的 sender 应该是实际发言人的昵称
3. 如果消息是图片，描述为 [图片]
4. 如果消息是表情/贴纸，描述为 [表情]
5. 如果消息是语音，描述为 [语音]
6. 如果消息是视频，描述为 [视频]
7. 如果消息是链接，保留完整链接文本
{extra_instructions}"""

# 快速读取模式（只读取当前可见消息，不滚动）
READ_CHAT_QUICK_EXTRA = """
【快速读取模式】
历史消息已经记录在数据库中，你只需要读取当前屏幕可见的消息即可。
- 不要向上滚动查看更早的消息
- 只读取当前屏幕上能看到的最新消息
- 重点关注最近几条新消息"""

# 完整读取模式（需要滚动获取更多历史）
READ_CHAT_FULL_EXTRA = """
【完整读取模式】
这是首次读取该聊天的历史，请尽可能多地获取聊天记录。
- 向上滚动查看更多历史消息
- 至少滚动 2-3 次，确保获取足够的历史上下文"""

# ── 发送回复提示词 ──
# 用于在设备上发送消息
SEND_REPLY_PROMPT = """请在当前聊天窗口发送以下消息：

{reply}

操作步骤：
1. 点击底部的输入框
2. 输入上述消息内容
3. 点击发送按钮
4. 确认消息已成功发送

注意：
- 如果输入框没有显示，可能需要先点击屏幕底部的输入区域
- 确保消息完整输入后再点击发送
- 发送后确认消息出现在聊天记录中"""

# ── 打开聊天软件提示词 ──
OPEN_APP_PROMPT = """请打开{app_name}应用。

如果应用已经在运行，确保它在前台显示。
如果应用未安装，报告错误。"""

# ── 进入聊天窗口提示词 ──
# 用于导航到指定的聊天对象
ENTER_CHAT_PROMPT = """请在{app_name}中找到并进入与"{target_chat}"的聊天窗口。

查找策略（按优先级尝试）：
1. 首先在聊天列表中上下滑动浏览，看是否能直接找到"{target_chat}"
2. 如果列表中没有找到，使用搜索功能：点击搜索图标，输入"{target_chat}"进行搜索
3. 点击搜索结果或列表中的对应聊天
4. 确认已进入正确的聊天窗口

注意：
- 优先尝试滑动列表查找，因为目标可能就在列表中只是不在当前可见区域
- 如果是群聊，确保群名匹配
- 如果是私聊，确保联系人名匹配
- 滑动时每次滑动幅度不要太大，以便能看清列表内容
- 如果多次搜索仍找不到，再报告错误"""

# ── 判断聊天类型提示词 ──
# 用于判断当前是单聊还是群聊
DETECT_CHAT_TYPE_PROMPT = """请判断当前聊天窗口是单聊还是群聊。

观察以下特征：
- 群聊通常有多个不同的发送者
- 群聊顶部可能显示群名和成员数
- 单聊只有一个对话对象

返回 JSON 格式：
{{"chat_type": "single" 或 "group", "chat_name": "群名或联系人名"}}"""
